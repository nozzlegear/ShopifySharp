#nullable enable
namespace ShopifySharp.GraphQL;
using System;
using System.Text.Json.Serialization;
using System.Collections.Generic;

/// <summary>
/// Store usage for app subscriptions with usage pricing.
/// </summary>
public record AppUsageRecord : IGraphQLObject, INode
{
    /// <summary>
    /// The date and time when the usage record was created.
    /// </summary>
    [JsonPropertyName("createdAt")]
#if NETSTANDARD2_0
	[System.Text.Json.DateOnlyConverter]
#endif
    public DateTime? createdAt { get; set; } = null;

    /// <summary>
    /// The description of the app usage record.
    /// </summary>
    [JsonPropertyName("description")]
    public string? description { get; set; } = null;

    /// <summary>
    /// A globally-unique ID.
    /// </summary>
    [JsonPropertyName("id")]
    public string? id { get; set; } = null;

    /// <summary>
    /// A unique key generated by the client to avoid duplicate charges.
    /// </summary>
    [JsonPropertyName("idempotencyKey")]
    public string? idempotencyKey { get; set; } = null;

    /// <summary>
    /// The price of the usage record.
    /// </summary>
    [JsonPropertyName("price")]
    public MoneyV2? price { get; set; } = null;

    /// <summary>
    /// Defines the usage pricing plan the merchant is subscribed to.
    /// </summary>
    [JsonPropertyName("subscriptionLineItem")]
    public AppSubscriptionLineItem? subscriptionLineItem { get; set; } = null;
}